package com.cay.rockstock.config;

public interface Constants {

     long ONE_MINUTE_SECONDS = 60;
     long ONE_HOUR_SECONDS = 60 * ONE_MINUTE_SECONDS;
     long TWO_HOUR_SECONDS = 2 * ONE_HOUR_SECONDS;
     long ONE_DAY_SECONDS = 24 * ONE_HOUR_SECONDS;
     long ONE_WEEK_SECONDS = 7 * ONE_DAY_SECONDS;
     long ONE_MONTH_SECONDS = 31 * ONE_DAY_SECONDS;
     long ONE_YEAR_SECONDS = 365 * ONE_DAY_SECONDS;

}
